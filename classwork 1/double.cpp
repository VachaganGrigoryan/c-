#include <iostream>

int main()
{
    double x = 5;

    // / for doubles is real division

    std::cout << 5 / 2; //prints 2
    std::cout << 5.0 / 2; // prints 2.5
    std::cout << 5 / 2.0; // prints 2.5
    std::cout << x / 2; //prints 2.5


    double y = 6.8;

    int n = y;

    std::cout << n; //prints 6
}