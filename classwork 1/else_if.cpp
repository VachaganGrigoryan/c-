#include <iostream>

int main()
{
    int n;
    std::cin >> n;
    
    if(n == 1)
    {
        std::cout << "One";
    }
    else if(n == 2)
    {
        std::cout << "Two";
    }
    else if(n == 3)
    {
        std::cout << "Three";
    }
    else
    {
        std::cout << "Error";
    }
        
  
}